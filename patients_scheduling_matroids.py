def patientScheduling(patients, slots):
    # Define a função de independência
    def isIndependent(S):
        slotsUsed = set()
        for patient in S:
            if patient[1] in slotsUsed:
                return False
            slotsUsed.add(patient[1])
        return True

    # Checa se o paciente já foi alocado
    def isAllocated(P):
        patientsAllocated = set()
        for patient in P:
            if patient[0] in patientsAllocated:
                return False
            patientsAllocated.add(patient[0])
        return True

    # Inicializa a lista de pacientes alocados e o custo total
    assignment = []
    allocated_patients = []
    totalCost = 0

    # Executa o algoritmo guloso para matroides
    for patient in sorted(patients, key=lambda x: x[2]):
        if isAllocated(allocated_patients + [patient]):
            if isIndependent(assignment + [patient]):
                assignment.append(patient)
                allocated_patients.append(patient)
                totalCost += patient[2]

    # Imprime a lista de pacientes alocados e o custo total
    for i in range(len(assignment)):
        print("O paciente {} sera alocado no horario {}".format(
            assignment[i][0], assignment[i][1]))
    print("Custo total: {}".format(totalCost))

